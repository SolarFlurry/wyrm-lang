#include "vm/chunk.h"
#include "vm/debug.h"
#include "vm/vm.h"
#include "compiler/compiler.h"

#include <stdio.h>
#include <stdlib.h>

int main (int argc, const char** argv) {
	if (argc != 2) {
		printf("Expected 1 argument\n");
		exit(1);
	}
	FILE* fp = fopen(argv[1], "r");
	if (fp == NULL) {
		printf("Could not open file %s\n", argv[1]);
		exit(1);
	}

	fseek(fp, 0, SEEK_END);
	size_t fileSize = ftell(fp);
	fseek(fp, 0, SEEK_SET);

	char* fileContents = (char*)malloc(sizeof(char) * (fileSize + 1));
	size_t charsRead = fread(fileContents, sizeof(char), fileSize, fp);
	fileContents[fileSize] = '\0';

	Chunk bytecode = compile(fileContents, argv[1]);

	disassembleChunk(&bytecode, "Generated Bytecode");
	
	initVM();

	evaluate(&bytecode);

	freeVM();
	freeChunk(&bytecode);

	return 0;
}